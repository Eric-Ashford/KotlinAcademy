//Lambda is just another way to write functions

fun main(){
    //By Eric Ashford
    println("======================================Program Start=====================================")

    val addNumbers = sum(5,7)
    println(addNumbers)

    println("List of Numbers")
    val listOfNumbers : List<Int> = listOf(1,2,3,4,5)

    //Normal for loop
    /*
    for(num in listOfNumbers){
        println(num)
    }
    */

    //Lambda for loop
    listOfNumbers.forEach {num ->
        println(num)
    }


    println("======================================Program End========================================")
}

//Normal function
/*
fun sum(num1:Int, num2:Int):Int{
   return num1 + num2
}
*/

//Lambda function
val sum = {number1:Int, number2: Int ->
    number1 + number2
