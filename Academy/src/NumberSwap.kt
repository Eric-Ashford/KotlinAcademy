//Swap numbers of variables X and Y

fun main(){
println("====================================Start Program==================================")

    var numX: Int? = 10
    var numY: Int? = 20
    var numTemp: Int?

    println("Slot 1: $numX, Slot 2: $numY")

    numTemp = numX
    numX = numY
    numY = numTemp

    println("Slot 1: $numX, Slot 2: $numY")
println("====================================End Program====================================")
}

/*
========================================================================================================================
======================================================Results===========================================================
========================================================================================================================

Slot 1: 10, Slot 2: 20
Slot 1: 20, Slot 2: 10

========================================================================================================================
====================================================Results End=========================================================
========================================================================================================================
