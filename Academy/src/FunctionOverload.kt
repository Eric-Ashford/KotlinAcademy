fun add(num1:Int, num2:Int):Int{
    return num1 + num2
}

fun add(num1:Int, num2:Int, num3:Int):Int{
    return num1 + num2 + num3
}

fun add(num1:Int, num2:Int, num3:Int, num4:Int):Int{
    return num1 + num2 + num3 + num4
}

fun add(vararg nums:Int): Int {
    var temp: Int = 0
    for(num in nums){
        temp += num
    }
    return temp
}

