//Step is to print every X iteration depending on the number of steps

fun main(){
    //By Eric Ashford
    println("Start App")


    println("=============================Going Up=================================")
    for(i: Int in 0..50 step 5){
        println(i)
    }
    println("\n===============================Going Down=======================================")

    for(i: Int in 50 downTo 0 step 5){
        println(i)
    }
    println("End App")
}

